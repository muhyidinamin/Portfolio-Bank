		<center> <h1> <b><font face ="Times new roman" color="black"> Kebijakan Privasi dan Pengamanan (Online Privacy and Security) CentralBank </b> </h1> </center><br>

		<td><center><h3>Berikut ini adalah kebijakan dan praktek yang dilakukan PT CentralBankBank, Tbk  untuk menunjukkan komitmen CentralBank di dalam menjaga dan memelihara
		privasi dan keamanan Anda pada saat Anda mengunjungi situs dan internet banking CentralBank.</center></h3></td><br>
		
		
		<td><b>1.Kebijakan Privasi</b></td><br>
		
		<td>CentralBank tidak menjual, menukar atau memperlihatkan segala informasi yang berkaitan dengan nasabah atau pengunjung situs dan internet banking CentralBank.
			CentralBank tidak melacak pengunjung situs CentralBank.
			Selama Anda login ke internet banking CentralBank, CentralBank akan menggunakan cookie yang akan berakhir pada saat Anda logout.
			Semua transaksi perbankan Anda dan informasi rekening lainnya disimpan secara rahasia sesuai dengan ketentuan hukum yang berlaku di Indonesia
			serta kebijakan internal CentralBank.</td><br><br>
		<td><b>2.Alamat E-mail dan Proteksi Informasi Transaksi<b></td><br>
		
		<td>CentralBank mewajibkan Anda untuk memberikan alamat e-mail Anda kepada CentralBank. CentralBank akan menggunakan alamat e-mail Anda untuk mengirimkan informasi
			atas transaksi finansial yang telah Anda lakukan melalui internet banking CentralBank dan beberapa transaksi non finansial yaitu saat login pertama kali, perubahan alamat e-mail, tambah koneksi, dan lain-lain. 
			Selain itu CentralBank juga menggunakan alamat e-mail Anda untuk kepentingan promosi, undangan gathering dan informasi-informasi lainnya.
			Untuk memastikan proteksi pengiriman informasi ke alamat e-mail Anda, mohon lakukan hal-hal sebagai berikut:
			Berikan kepada CentralBank alamat e-mail pribadi Anda. Jangan menggunakan alamat e-mail palsu.
			Ubahlah segera alamat e-mail Anda di internet banking CentralBank jika Anda mengganti alamat e-mail Anda.
			Jika Anda menghubungi CentralBank melalui e-mail, jangan mengirimkan informasi rekening Anda yang sifatnya rahasia atau sensitif, 
			termasuk PIN Anda.</td><br><br>
		
		<td><b>3.Situs BCA</b></td><br>
		<li><td>Di dalam situs CentralBank, CentralBank menyediakan URL link ke situs lain yang tidak dikontrol oleh CentralBank. CentralBank tidak bertanggung jawab
			atas isi dan keamanan dari situs tersebut. Jika Anda mengakses situs tersebut,silakan memeriksa kebijakan privasi dan pengamanan mereka.
			Dalam hal Anda mengakses situs atau internet banking CentralBank melalui URL link dari situs lain, pastikan kebenaran alamat yang Anda akses 
			yaitu centralbank.web.id</li></td><br><br>

		<li><td>CentralBank dapat mengubah kebijakan privasi dan informasi pengamanan ini setiap saat untuk tetap menyesuaikan dengan situasi 
		dan teknologi terbaru. Anda selalu dapat meninjau informasi dan kebijakan privasi CentralBank yang terbaru di centralbank.web.id</li></td><br><br> 
		
